// ========== Helpers ==========
export function getAnimalImage(
  animalType: string,
  age: 'baby' | 'adult' = 'adult',
  gender: 'male' | 'female' = 'male'
): string {
  if (age === 'baby') {
    return BABY_ANIMAL_IMAGES[animalType] || BABY_ANIMAL_IMAGES.lion;
  }
  if (gender === 'male') {
    return MALE_ANIMAL_IMAGES[animalType] || ADULT_ANIMAL_IMAGES.lion;
  }
  return FEMALE_ANIMAL_IMAGES[animalType] || ADULT_ANIMAL_IMAGES.lion;
}

export function getShopItemImage(itemId: string, itemType: string): string {
  if (itemType === 'habitat') {
    return HABITAT_IMAGES[itemId] || adultLion;
  }
  if (itemType === 'decoration') {
    return DECORATION_IMAGES[itemId] || adultLion;
  }
  if (itemType === 'food') {
    return FOOD_IMAGES[itemId] || adultLion;
  }
  if (itemType === 'toy') {
    return TOY_IMAGES[itemId] || adultLion;
  }
  return adultLion;
}

